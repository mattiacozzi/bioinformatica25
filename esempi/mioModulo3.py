#questo modulo contiene le funzioni della mia calcolatrice
import math

def somma(a, b):
    ans = a + b
    return ans

def differenza(a, b):
    ans = a - b
    return ans

def prodotto(a, b):
    ans = a * b
    return ans

def quoziente(a, b):
    ans = a / b
    return ans

def radiceQuad(a):
    ans = math.sqrt(a)
    return ans

def radiceCub(a):
    ans = math.cbrt(a)
    return ans

def quadrato(a):
    ans = pow(a, 2)
    return ans

def cubo(a):
    ans = pow(a, 3)
    return ans

def pot(a, b):
    ans = pow(a, b)
    return ans

def logaritmo(a, b):
    ans = math.log(a, b)
    return ans

def logNat(a):
    ans = math.log(a)
    return ans

def expo(a):
    ans = pow(math.e, a)
    return ans

def seno(a):
    ans = math.sin(a)
    return ans

def coseno(a):
    ans = math.cos(a)
    return ans

def tangente(a):
    ans = math.tan(a)
    return ans






