alberi1 = {"Quercia","Pino","Faggio","Ontano","Castagno"}
print(alberi1)
alberi2 = {"Ontano","Castagno","Acero","Ciliegio"}
print(alberi2)
alberiUnion = alberi1.union(alberi2)
print("Ecco l'unione tra i due insiemi di alberi")
print(alberiUnion)
alberiIntersection = alberi1.intersection(alberi2)
print("Ecco l'intersezione tra i due insiemi di alberi")
print(alberiIntersection)